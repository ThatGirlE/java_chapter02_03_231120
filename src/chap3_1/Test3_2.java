package chap3_1;

public class Test3_2 {

	public static void main(String[] args) {
		
		//증감 연산자 ++, --
		
		int a = 0;
		
		int b = ++a; // a = a + 1
		
		int c = 10;
		
		int d = --c; // c= c- 1
		
		System.out.println("b: "+b);
		System.out.println("d: "+d);
		System.out.println("a: "+a);
		System.out.println("c: "+c);
		
		
		
		
		
	}

}
